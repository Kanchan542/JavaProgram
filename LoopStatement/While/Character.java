class Character{
	public static void main(String args[]){
		System.out.println("**************** UpperCase From A to Z *****************");
		char ch='A';
		while(ch<='Z'){
			System.out.println(ch);
			ch++;
		}
		System.out.println("********** LowerCase from a to z ****************");
		char ch1='a';
		while(ch1<='z'){
			System.out.println(ch1);
			ch1++;
		}
	}
}